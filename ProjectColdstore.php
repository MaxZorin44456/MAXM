                        <h2>Video Game Collecting & Archiving</h2>
                        <span class="byline">AKA "Project Coldstore"</span>
                    </header>
                    <p>Over the years I've collected a fair few games - most of these come from the 90's and early 2000's, some in big box format while others are in jewel cases or alternately DVD cases. Below you can see what games I currently own.</p>
                    <p>In respect to this I am currently working on a project called "Project Coldstore" where I hope to get pictures and scans of the below games, their disks, boxes, manuals and other associated content (such as advertising materials, warranties etc.) The main aim behind this is to retain as much information as possible about these games which is supplementary to the actual games themselves as while the games will likely live on the boxes, discs and other supplements will eventually expire and be lost forever which is a bit of a shame... hence my one-man digital conservation effort of sorts.</p>
                    <p>Anyhow, feel free to have a look through the list below, the links will take you to individual pages for each of the games wherein you can find the pictures, scans and additional information.</p>
                    
