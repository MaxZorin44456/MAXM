                    <header><h3><b>The History</b></h3></header>
                    <p>Since the late 90's I've always been a big fan of trains, both model and otherwise and while my interest in train watching has waned significantly since my younger years my interest in model railways has been fairly steady, albeit lacking in what could be classified as "active" participation.</p>
                    <p>I was originally introduced to model railways when I received my train first set, the "Thomas The Tank Engine" set, following that I spent a great deal of my time going over Hornby brochures, mooching round model shops and otherwise occupying my time where possible with looking at stuff related to model railways, but as you may have noticed by this point I have yet to mention actually building a layout which leads me onto the next part.</p>
                    <p>For most of my childhood (and life to be honest) my rolling stock has sat in storage, occasionally it'd see the light of day and back it would go into a box waiting for the blue moon moment it actually got to trundle down track - on the rare occasion wheels did make contact with rails it would always be on the surface most likely to inspire high levels of cringe in model railway enthusiasts, the carpet. This situation is primarily due to space constraints as I've never had the room in which to construct a permanent or large collapsable/storable layout and until recently I've never really had the financial means to do it to a degree I would view as "right" either. As such until a few years ago my interest in the hobby had wained significantly. Things, however, have changed and after some research, some planning and some encouragement I've begun the process of setting up a loft layout & general purpose storage area.</p>
                    
                    <header><h3><b>The Layout</b></h3></header>
                    
                    <p>I guess it is time to get down to business and explain some details. The layout itself is a loft layout and doesn't really have much in the way of a set period nor a specific operational region, as such the only real concrete rules I do have regarding my layout is that it's going to be primarily diesel and electric based (I'm not a big fan of steam locomotives), is going to feature both freight and passenger operations, is going to be post-Beeching and most likely will feature a variety of liveries. Beyond that I've only really settled on a name which is vaguely Scottish sounding and that I'm planning on having a large double-track loop going round the entirety of the loft with additional facilities put into place once I've gotten my head around the layout planning software "AnyRail" and have a good idea as to how much space I'll be looking at being able to use.<p>
                    
