                    <p>I originally started this project prior to 2014 at the behest of somebody else. The original intention was to create the entire series of maps using customised blocks and textures from the original games for a multiplayer server mod in Minecraft, however, due to changes in my mood I parted ways with this individual and set out to create the series of maps in my own time.</p>
                    <p>Regarding the above the first map I ever created from the series was "Mansion" from the first Timesplitters game for PS2, this used a heavy combination of customised blocks with custom textures and microblocks and was only properly playable within a customised Minecraft client. Resulting the above split my second map created was "Chasm" from the Timesplitters 2 game, this was released on Planet Minecraft in early 2014. Following a brief break in which I worked on other projects I came back to the idea of creating Timesplitters maps just under a year later and underwent the process of converting and massively overhauling the original map I produced, replacing all of the custom blocks where required and ensuring the exterior and interior of the building matched that of the console game equivalent as much as possible. These changes resulted in the release of the second map "Mansion" onto Planet Minecraft in early 2015.</p>
                    <p>Following the release of "Mansion" the next four maps under the "Timesplitters Recreation Project" to be released were "Compound" from Timesplitters 2 in early 2015, "Streets" from Timesplitters and Timesplitters 2 in late 2015 and lastly "Subway" from Timesplitters: Future Perfect a few days before Christmas 2015. Links to all of these maps can be found below and can be downloaded with ease from the relevant "Planet Minecraft" pages.</p>
                    
