					<p>The idea to recreate Dentonside in Minecraft originally spurred from an idea in January 2014 to recreate the entirety of Malton from the MMORPG "UrbanDead." Initially I wished to start in the suburb of Ridleybank and work out however shortly after construction began I started to realise as to how much of a daunting task this would be for a single person. After crunching the numbers I roughly estimated that it would take me at least a day per building and given there were one hundred buildings per suburb and one hundred suburbs at my rate of construction it would take me approximately 27 years to complete the project. After this discovery I decided to shelve the idea of recreating the entirety of Malton and instead decided to focus on the suburb of "Dentonside" instead as this had been my stomping ground during the majority of my tenure within Malton as a survivor and thus the "Dentonside Recreation Project" was born.</p>
                    
                    <p>By November 2014 I had quickly realised that while a large majority of the existing buildings I had constructed for Ridleybank could be repurposed for Dentonside one building remained for which no use could be found. As Dentonside was not host to a single nightclub the nightclub known as "Club Vaughan" was of no use thus I uploaded this building seperately to Planet Minecraft. The main project itself however continued smoothly and eventually I decided that on top of the existing project I would endevor to produce an alternate version of the map set a year after the quarantine of Malton/Dentonside during the winter of 2006.</p>
                    
                    <p>By July 2016 the first part of the "Dentonside Recreation Project" was almost complete, the remaining work that needed to be done primarily revolved around furnishing buildings and by that point I had set myself a completion deadline of September. Several months later in September the first part of the project was fully complete and released onto Planet Minecraft.</p>
                    
                    <p>Following the release of the first part of the project I began work on the second part of the project, this required converting the biome of the map into that which would result in it snowing - while this was easily achieved the next phase was ensuring an appropriate covering of snow across the map. As to ensure every part of the map was covered I had to manually move around the map while it snowed and fill in the spots not touched until complete cover was acheived. Following this the map was updated to show some minor "decay", such as broken windows, snow coming into properties and car crashes/barricaded buildings. This work was completed and the final checks were carried out in May 2017 which saw the release of the second part of the project "Dentonside - 1 Year After" which can be downloaded below along with its counterpart set shortly after the quarantine.</p>
                    
