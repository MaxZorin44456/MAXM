                    <b><p>April 2016</p></b>
                            
                            <p>This month I decided to put a new idea into practice which is a monthly update on my “work” over the previous month which will be posted to both Tumblr and my website (in addition to anything else I decide to post) on the first of every new month.</p>
                            
                            <p>This month has been kind of a mixed bag - I’ve finally been able to finish off the majority of the terraforming for “Elywof Island” due to an update for both Bukkit and VoxelSniper (which in a nice coincidence also allowed me to upgrade my Bukkit profile and by proxy the map to Minecraft version 1.9.2) so now all of the coastline and island interior have been fully fleshed out. which leaves sorting out the agricultural land, urban areas, trees, biomes, caves and dealing with the sea surrounding the island.</p>
                            
                            <p>Following from the last point most of the work outside terraforming on “Elywof Island” has been spent working on “Droff Wharf” - the harbour has been revamped and now features seawalls, a control tower and the ability for passengers on foot to board “The Wise Traveller” from a designated terminal which is still under construction, it has also been provided with a flag and connected by path to Elywof City and several improvements (mostly beautification) have been made to the railway station as well. A minor change to note is that the second rail side farmhouse outside of Osinwar has been completed and some beet fields have been added.</p>
                            
                            <p>The “Dentonside/Urban Dead” recreation project has taken a back seat this month, some work was completed on some buildings bringing the total initial construction to 75%, however, asides from this not much has changed inside that project this month.</p>
                            
                            <p>The interactive fiction game “The Village” has been renamed to “Just You and The Dust Now…” and while I have some further ideas on what to do with it beyond what has already occurred I haven’t got around to working on it further yet.</p>
                            
                            <p>I’ve begun transferring the saves for Timesplitters 2 that I have on my Gamecube memory cards to HTML files, this has also resulted in my website being updated as to hopefully incorporate these files once I’m finished with them all.</p>
                            
                            <p>In addition to this I’ve spent some time getting some personal matters attended to - mainly flooring some more of my loft and organising it so everything wasn’t just heaped in a pile - that has unfortunately been a VERY long time coming but did please me greatly - primarily because I love organising things and secondly because I found my original PS1 which I was under the presumption had been lost in 2013. In addition to that, I am very pleased to announce I will be travelling to Rome for the first time in my life and Italy for the second time in my life this September.</p>
                            
                            <p>Anyhow, for those of you who are interested my plans of the month of May are as follows:</p>
                            <p>Maybe start work on the radio and TV scripts for my “Scottish Highland” based map for Project Zomboid - this is highly dependent on “WordZed” being released as I don’t want to start putting fingers to the keyboard if I have no idea as to what sort of restrictions the software will have in regards to sentence length, paragraph length etc.</p>
                            <p>In Minecraft I am planning on finishing “The Wise Traveller” this month and hopefully starting to get a framework of streets and building foundations laid out for Droff Wharf so work can eventually commence at some point on building the town. I am also planning on starting planning on additional housing outside the city walls in Elywof City and getting some more of the railway infrastructure completed in Droff Wharf (e.g. sheds and things.) I also plan to hopefully work further on Dentonside however this depends on if I can be bothered. I am hoping to at least get the buildings all completed for furnishing by September however this depends on me being able to continuously build things which gets kind of stale after a while and leaves me needing to take a break.</p>
                            
                            <p>I am planning on hopefully getting some of the Timesplitters 2 HTML files uploaded to my website and getting around to fixing up the “Timesplitters” page so it is less of a mess and in addition to this generally trying to get the website up to scratch.</p>
                            
                            <p>Lastly I am planning to update “Just You and The Dust Now…” however this relies on me being able to get past the usual plethora of excuses I give myself to not work on it or do other things like go cycling or start work on all those additional ideas and projects I’ve added to my “projects” folder over the past year.</p>
                            
                            <p>Anyhow, this has been my monthly update - I’ll catch you next month.</p>                    

