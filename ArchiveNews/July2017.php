                            <p>The third change is that I've cleaned the site up a bit so the links should now take you to pages which don't include ".php" in the address bar.</p>
                            
                            <p><b>Minecraft</b></p>
                            
                            <p>The first update which also happens to be the shortest is the completion and release of the "Dentonside - 1 Year After" project to Planet Minecraft. The link to that project can be found below:</p>
                            
