                    <b><p>July 2017</p></b>
                            
                            <p>As you may have noticed there has been a distinct lack of monthly updates on the site, while this may seem like an indicator that work has not been progressing on my various projects, rest assured updates have been occurring behind the scenes - albeit at a slightly slower pace than I'd like, the reasons for which are outlined below.</p>
                            
                            <p><b>Personal Stuff</b></p>
                            
                            <p>It's not a frequent occurrence that I post on this site about my personal life, not because I dislike talking about myself because to be honest this site (MaxZorin44456.com) is in effect a vanity project and if you get me going I'll yabber about myself all day but instead I just don't really see it as being all too important unless it's impacting something on here... which given things recently have been affecting the content and output here I guess it's time to take the plunge and discuss my personal life a bit.</p>
                            
                            <p>Anyhow, the reason for the lack of updates of late is two fold, firstly for the past few months, I've been in a bit of a lull in terms of just being bothered to do anything. While I'm not sure of the exact reason for this malaise it's basically glassed my desire to get anything done besides sleep for thirteen hours every time Friday/Saturday rolls around and spent Sunday catching up on everything I wanted to do on Saturday before having the whole mess derail come Monday. As you can probably tell, this isn't too productive hence why I'm typing up my first "monthly update" in over three months. Alas, things seem to be slowly getting back on track so it could be worse.</p>
                            
                            <p>The second reason for the lack of updates is due to the big ol' C. No, not Communism, instead, Cancer has decided to make an appearance in my life beyond the occasional advert on TV from a cancer charity. Now to clarify I don't have cancer, instead, my Mum does.</p>
                            
                            <p>As to break the story down a bit, over a month ago my mum started complaining about having issues swallowing food, things seemed to be getting stuck/taking their time to pass into her stomach or alternately being thrown back up which prompted a doctors appointment. Following this blood work was taken, nothing seemed to be amiss, however, an endoscopy was scheduled which lead to visual confirmation that Esophageal cancer appeared to be present, biopsies later confirmed this. Following two MRI's, a PET scan, a surgical consultation, indications of an additional pancreatic cancer which turned out to be nothing and finally a week ago an oncological consult we're looking towards several months of chemotherapy followed by keyhole surgery and following that a long recovery period.</p>
                            
                            <p>So yeah, things could be better... nevertheless, I have still been progressing on things behind the scenes and as to balance out the bad I've got some nice good changes - the main changes being pictures in the monthly update instead of just bog standard text.</p>
                            
                            <p><b>MaxZorin44456.com</b></p>
                            
                            <p>Okay, so first change to note is that the website has seen a slight modification in the inclusion of images to the monthly update. In addition to that, I've updated one of the project pages which will be explained in the relevant section below.</p>
                            
                            <p>The second change to note is the inclusion of a new phplist mailing list. You can sign up for that on the page below and once signed up you will receive the monthly update straight to your inbox too.</p>
                            
